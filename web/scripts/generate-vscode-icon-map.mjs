#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const generatedIconsDir = path.join(projectRoot, 'src/lib/icons/vscode-generated');
const generatedModulePath = path.join(projectRoot, 'src/lib/vscode-icon-map.generated.ts');
const generatedReadmePath = path.join(generatedIconsDir, 'README.md');

// Keep special app files distinct even when markdown icons are hidden.
const APP_FILE_OVERRIDES = {
  'agents.md': 'file_type_agents.svg',
  'claude.md': 'file_type_claude.svg',
};

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function cleanDir(dir) {
  fs.rmSync(dir, { recursive: true, force: true });
  fs.mkdirSync(dir, { recursive: true });
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function readFileIfExists(filePath) {
  try {
    return fs.readFileSync(filePath);
  } catch {
    return null;
  }
}

function writeFileIfChanged(filePath, content) {
  const next = typeof content === 'string' ? Buffer.from(content, 'utf8') : Buffer.from(content);
  const prev = readFileIfExists(filePath);
  if (prev && Buffer.compare(prev, next) === 0) return false;
  fs.writeFileSync(filePath, next);
  return true;
}

function copyFileIfChanged(srcPath, destPath) {
  const next = fs.readFileSync(srcPath);
  const prev = readFileIfExists(destPath);
  if (prev && Buffer.compare(prev, next) === 0) return false;
  fs.writeFileSync(destPath, next);
  return true;
}

function safeLower(value) {
  return String(value ?? '').toLowerCase();
}

function iconFileFromDefinition(definitions, id) {
  const def = definitions[id];
  const relPath = def?.iconPath;
  if (!relPath) return null;
  return path.basename(relPath);
}

function toIconMap(rawMap, definitions) {
  const out = {};
  for (const [key, id] of Object.entries(rawMap ?? {})) {
    const file = iconFileFromDefinition(definitions, id);
    if (!file) continue;
    out[safeLower(key)] = file;
  }
  return out;
}

function buildMappings(materialTheme) {
  const defs = materialTheme.iconDefinitions ?? {};

  const fileNames = toIconMap(materialTheme.fileNames, defs);
  const fileExtensions = toIconMap(materialTheme.fileExtensions, defs);

  const folderNames = {
    ...toIconMap(materialTheme.folderNames, defs),
    ...toIconMap(materialTheme.rootFolderNames, defs),
  };

  const folderNamesExpanded = {
    ...toIconMap(materialTheme.folderNamesExpanded, defs),
    ...toIconMap(materialTheme.rootFolderNamesExpanded, defs),
  };

  const defaultFile = iconFileFromDefinition(defs, materialTheme.file) ?? 'file.svg';
  const defaultFolder = iconFileFromDefinition(defs, materialTheme.folder) ?? 'folder.svg';
  const defaultFolderExpanded =
    iconFileFromDefinition(defs, materialTheme.folderExpanded) ?? 'folder-open.svg';

  return {
    defaultFile,
    defaultFolder,
    defaultFolderExpanded,
    fileNames,
    fileExtensions,
    folderNames,
    folderNamesExpanded,
  };
}

function writeGeneratedModule(mappings, copiedIcons) {
  const lines = [];
  lines.push('// AUTO-GENERATED by scripts/generate-vscode-icon-map.mjs');
  lines.push('// Do not edit by hand.');
  lines.push('');

  const importVarByFile = new Map();
  copiedIcons.forEach((file, idx) => {
    const varName = `icon_${idx}`;
    importVarByFile.set(file, varName);
    lines.push(`import ${varName} from '$lib/icons/vscode-generated/${file}';`);
  });

  lines.push('');

  const emitMap = (name, mapObj) => {
    lines.push(`export const ${name}: Record<string, string> = {`);
    for (const [k, v] of Object.entries(mapObj).sort((a, b) => a[0].localeCompare(b[0]))) {
      lines.push(`  ${JSON.stringify(k)}: ${importVarByFile.get(v)},`);
    }
    lines.push('};');
    lines.push('');
  };

  lines.push(`export const DEFAULT_FILE_ICON = ${importVarByFile.get(mappings.defaultFile)};`);
  lines.push(`export const DEFAULT_FOLDER_ICON = ${importVarByFile.get(mappings.defaultFolder)};`);
  lines.push(
    `export const DEFAULT_FOLDER_OPEN_ICON = ${importVarByFile.get(mappings.defaultFolderExpanded)};`
  );
  lines.push('');

  emitMap('FILE_NAME_ICONS', mappings.fileNames);
  emitMap('FILE_EXTENSION_ICONS', mappings.fileExtensions);
  emitMap('FOLDER_NAME_ICONS', mappings.folderNames);
  emitMap('FOLDER_NAME_OPEN_ICONS', mappings.folderNamesExpanded);

  return writeFileIfChanged(generatedModulePath, `${lines.join('\n')}\n`);
}

function writeReadme(materialVersion, vscodeIconsCommit, copiedIcons) {
  const body = `# Material Icons (Generated Subset)\n\nThis directory is auto-generated by:\n- \`web/scripts/generate-vscode-icon-map.mjs\`\n\nBase source:\n- \`material-icon-theme@${materialVersion}\`\n\nOverlay source:\n- \`vscode-icons\` for app-specific filename overrides\n- upstream commit (informational): \`${vscodeIconsCommit}\`\n\nIncluded SVG files:\n${copiedIcons.map((f) => `- \`${f}\``).join('\n')}\n\nLicense:\n- Material Icon Theme: MIT\n- VSCode Icons: MIT\n`;
  return writeFileIfChanged(generatedReadmePath, body);
}

function main() {
  const materialEntryPath = require.resolve('material-icon-theme', { paths: [projectRoot] });
  const materialRoot = path.resolve(path.dirname(materialEntryPath), '..', '..');
  const materialPkgPath = path.join(materialRoot, 'package.json');
  const materialPkg = readJson(materialPkgPath);
  const materialThemePath = path.join(materialRoot, 'dist/material-icons.json');
  const materialIconsDir = path.join(materialRoot, 'icons');

  const vscodeIconsPkgPath = require.resolve('vscode-icons/package.json', { paths: [projectRoot] });
  const vscodeIconsRoot = path.dirname(vscodeIconsPkgPath);
  const vscodeIconsDir = path.join(vscodeIconsRoot, 'icons');

  const mappings = buildMappings(readJson(materialThemePath));

  for (const [name, iconFile] of Object.entries(APP_FILE_OVERRIDES)) {
    mappings.fileNames[safeLower(name)] = iconFile;
  }

  const usedIcons = new Set([
    mappings.defaultFile,
    mappings.defaultFolder,
    mappings.defaultFolderExpanded,
    ...Object.values(mappings.fileNames),
    ...Object.values(mappings.fileExtensions),
    ...Object.values(mappings.folderNames),
    ...Object.values(mappings.folderNamesExpanded),
  ]);

  ensureDir(generatedIconsDir);

  const copied = [];
  let changedIconFiles = 0;
  for (const iconFile of Array.from(usedIcons).sort()) {
    const fromMaterial = path.join(materialIconsDir, iconFile);
    const fromVscodeIcons = path.join(vscodeIconsDir, iconFile);
    const target = path.join(generatedIconsDir, iconFile);

    if (fs.existsSync(fromMaterial)) {
      if (copyFileIfChanged(fromMaterial, target)) changedIconFiles += 1;
      copied.push(iconFile);
      continue;
    }

    if (fs.existsSync(fromVscodeIcons)) {
      if (copyFileIfChanged(fromVscodeIcons, target)) changedIconFiles += 1;
      copied.push(iconFile);
      continue;
    }

    throw new Error(`Missing icon asset: ${iconFile}`);
  }

  const currentFiles = new Set(copied);
  for (const existing of fs.readdirSync(generatedIconsDir)) {
    if (existing === 'README.md') continue;
    if (currentFiles.has(existing)) continue;
    fs.rmSync(path.join(generatedIconsDir, existing), { force: true });
    changedIconFiles += 1;
  }

  const moduleChanged = writeGeneratedModule(mappings, copied);

  const vscodeCommit = process.env.VSCODE_ICONS_COMMIT ?? 'unknown';
  const readmeChanged = writeReadme(materialPkg.version ?? 'unknown', vscodeCommit, copied);

  const changedFiles = changedIconFiles + (moduleChanged ? 1 : 0) + (readmeChanged ? 1 : 0);
  if (changedFiles === 0) {
    console.log(`Icon map already up to date (${copied.length} SVG assets).`);
  } else {
    console.log(`Generated Material-based icon map with ${copied.length} SVG assets (${changedFiles} files updated).`);
  }
}

main();
